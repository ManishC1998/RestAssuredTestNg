package pojo;

public class Put {
	
	String mAbout;
	String mArea;
	String mAbot;
	int mPin;
	int mid;
	public Put(String mAbout, String mArea, String mAbot, int mPin, int mid) {
		super();
		this.mAbout = mAbout;
		this.mArea = mArea;
		this.mAbot = mAbot;
		this.mPin = mPin;
		this.mid = mid;
	}
	public String getmAbout() {
		return mAbout;
	}
	public void setmAbout(String mAbout) {
		this.mAbout = mAbout;
	}
	public String getmArea() {
		return mArea;
	}
	public void setmArea(String mArea) {
		this.mArea = mArea;
	}
	public String getmAbot() {
		return mAbot;
	}
	public void setmAbot(String mAbot) {
		this.mAbot = mAbot;
	}
	public int getmPin() {
		return mPin;
	}
	public void setmPin(int mPin) {
		this.mPin = mPin;
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	
	
	
	

}
